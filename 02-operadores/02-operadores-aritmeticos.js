let result;

result = 3 + 9;             // Suma
console.log( result );

result = "3" + 9;             // Concatenacion
console.log( result );        // '39'

result = "3" - 9;             // Resta
console.log( result );

result = 3 * 9;             // Multiplicacion
console.log( result );

result = 3 / 9;             // Division
console.log( result );

result = 3 % 9;             // Modulo: Residuo de la division
console.log( result );

result = 3 ** 9;            // Exponenciacion > 3 * 3 * 3 * 3 * 3 * 3 * 3 * 3 * 3
console.log( result );