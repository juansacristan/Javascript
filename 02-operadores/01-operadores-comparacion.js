let compared;

// Comparaciones Matematico
compared = 9 > 1;
console.log( compared );

compared = 9 >= 1;
console.log( compared );

compared = 9 < 1;
console.log( compared );

compared = 9 <= 1;
console.log( compared );

// Comparaciones de Booleano
compared = 9;           // = Operador de asignacion
console.log( compared );

compared = 9 == 9;      // == Operador para comparar los valores
console.log( compared );

compared = 9 == 9;      // == Operador para comparar los valores
console.log( compared );

// Igualdad estricta
compared = 9 === 9;     // === Operador para comparar los valores y los tipos
console.log( compared );

compared = '9' === 9;   // Igualdad estricta
console.log( compared );

// Diferente estricto
compared = 9 != 1;      // Diferente de (compara valores)
console.log( compared );

compared = 9 !== 1;     // Diferente de (compara valores y tipos)
console.log( compared );

// ! NOR > Negacion
